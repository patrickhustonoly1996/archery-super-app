class HandicapCalculator {
  /// Calculate handicap for a given score and round type
  /// Returns null if round type not supported or score invalid
  static int? calculateHandicap(String roundTypeId, int score) {
    final table = _handicapTables[roundTypeId];
    if (table == null) return null;

    // Find closest handicap by binary search
    // Tables are ordered from best score (HC 0) to worst score (HC 150)
    for (int hc = 0; hc < table.length; hc++) {
      if (score >= table[hc]) {
        return hc;
      }
    }

    // Score below minimum in table
    return table.length - 1;
  }

  /// Get the theoretical maximum score for a handicap on a given round
  static int? getScoreForHandicap(String roundTypeId, int handicap) {
    final table = _handicapTables[roundTypeId];
    if (table == null) return null;
    if (handicap < 0 || handicap >= table.length) return null;
    return table[handicap];
  }

  /// Check if handicap calculation is supported for this round type
  static bool isSupported(String roundTypeId) {
    return _handicapTables.containsKey(roundTypeId);
  }

  /// Check if a round type is indoor
  static bool isIndoor(String roundTypeId) {
    return _indoorRounds.contains(roundTypeId);
